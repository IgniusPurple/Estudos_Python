import statistics

# 1 - Aplicar a média

print(statistics.mean([3,2,5,6,8,4,0,9]))


# 2 - Aplicar médiana
print(statistics.median([1, 2, 3, 5, 8, 6, 8, 9]))

# 3 - Aplicar a moda
print(statistics.mode([0,2,2,0,0,2,6,5,5,6,6,7,7,9,9,4,4,1,5,4,5,4,2,2,3]))

# 4  - Desvio padrão
print(statistics.stdev([1,1.5,2,2.5,3,3.5,4,4.5]))