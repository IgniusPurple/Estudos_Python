'''
Escreva um programa em Python que leia um número e
represente o número antecessor e sucessor desse número que foi lido, utilizando operadores de atribuição.

'''

number = int(input("Escreva um número: \n"))


print(f"O número digitado foi: {number} \nSeu Antecessor é o número: {number - 1} \nSeu Sucesssor é o número: {number + 1}")