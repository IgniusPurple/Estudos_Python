num1 = int(input("Digite o primeiro numero: "))
num2 = int(input("Digite o segundo numero: "))


# Aritiméticos 
sum = num1 + num2
sub = num1 - num2
div = num1 / num2
mult = num1 * num2
mod = num1 % num2
exp = num1 ** num2 

print (f"\n{sum} \n{sub} \n{div} \n{mult} \n{mod} \n{exp}")


# Comparação

bigger = num1 > num2
smaller = num1 < num2
equal = num1 == num2
different = num1 != num2
bigger_equal = num1 >= num2
smaller_equal = num1 <= num2

print(f"\n{bigger} \n{smaller} \n{equal} \n{different} \n{bigger_equal} \n{smaller_equal}")


# Atribuição

num1 += 1
num1 -= 1
num1 /= 1
num1 *= 1
